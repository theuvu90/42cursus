#include "libftprintf.h"

int	ft_putstr(char *s)
{
	int	i;	

	i = 0;
	if (!s)
		return (write(1, "(null)", 6));
	while (i < ft_strlen(s))
	{
		ft_putchar(s[i]);
		i++;
	}
	return (i);
}
